                                    </li>
                                    <li>
                                        <h5><b>Responsabilidades Funcionales</b></h5>
                                        <p>Las responsabilidades funcionales son una serie de actividades genéricas asociadas a cada área de expertise en que se divide la compañía.</p>
                                        <p>En Advanzer y Entuizer hemos clasificado las responsabilidades funcionales en 7 dominios.</p>
                                        
                                        <ul type="a">
                                            <li><p>Costo</p></li>
                                            <li><p>Tiempo</p></li>
                                            <li><p>Calidad</p></li>
                                            <li><p>Entregables</p></li>
                                            <li><p>Relación con clientes</p></li>
