                                <h5><b><a href="https://drive.google.com/a/advanzer.com/file/d/0B20xPQDaUxigSkFlLVl1WlBfQWM/view?usp=sharing" target="_blank">Modelo de Gesti&oacute;n de Desempeño</a></b></h5>
                                
                                <br>
                                
                                <p>El <b>Modelo de Gestión de Desempeño</b> (MGD) Advanzer contempla los elementos necesarios para asegurar que cada colaborador conozca, se responsabilice y actué acorde a lo que su rol demanda en la organización.</p>

                                <p>Como resultado, el MGD no solamente sirve como marco evaluativo sino como pauta para el <b>desarrollo integral</b> de los colaboradores de la organización.</p>

                                <p><b>Componentes del Modelo</b></p>

                                <p>El modelo contempla una interrelación y complementariedad de componentes para que cada persona en Advanzer:
                                    
                                    
                                </p>
                                
                                <ul type="square">
                                    <li><p>Cumpla con las responsabilidades inherentes al rol que se desempeña.</p></li>
                                    <li><p>De muestra de su comportamiento acorde a la expectativa de su posición.</p></li>
                                    <li><p>Logre los objetivos de desarrollo acordados durante el Feedback.</p></li>
                                </ul>
                                
                                <br>
                                
                                <p>Los cuatro componentes del modelo son:</p>
                                
                                <ul type="1">
                                    <li>
                                        <h5><b>Competencias Laborales</b></h5>
                                        <p>Una competencia es la capacidad de poner en práctica el conjunto de conocimientos, destrezas y actitudes que una persona posee para resolver los retos que se le presentan.</p>
                                        <p>En Advanzer y Entuizer hemos definido 15 competencias laborales que deben estar presentes en los colaboradores. Cada competencia se encuentra integrada por una definición conceptual, conductas clave y comportamientos observables asignables de acuerdo a las posiciones.
                                        </p>
